const float_t* gLoadedTable = TwelveTET;

/// @brief Convert note index to frequency.
float_t NoteToFreq(uint8_t note)
{
    uint8_t octave = 0;
    while (note >= 24)
    {
        octave++;
        note -= 24;
    }

    float_t freq = gLoadedTable[note];

    while (octave > 0)
    {
        freq *= 4.0f;
        octave--;
    }

    return freq;
}