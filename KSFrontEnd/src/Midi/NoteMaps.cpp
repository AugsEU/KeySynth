// This maps note layout enums to the data
constexpr const uint8_t* gNoteLayouts[] =
{
    gIsometricNoteLayout,
};





// ============================================================================
// Public functions
// ============================================================================
uint8_t KeyCodeToNoteNum(uint8_t keycode, NoteLayout layout)
{
    const uint8_t* keyToNote = gNoteLayouts[layout];
    return keyToNote[keycode];
}