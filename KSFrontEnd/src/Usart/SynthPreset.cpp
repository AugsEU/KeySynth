char gNameBuffer[PRESET_NUM_CHARS+1];



// ============================================================================
// Public functions
// ============================================================================
char* GetSubMemPresetName(size_t index)
{
	memcpy_P(gNameBuffer, &gSubPresets[index].mName, PRESET_NUM_CHARS);
	gNameBuffer[PRESET_NUM_CHARS] = '\0';

	return gNameBuffer;
}


void LoadSubMemPreset(size_t index)
{
	static_assert(sizeof(gSynthParamValues) >= sizeof(gSubPresets[index].mValues));
	memcpy_P(gSynthParamValues, &gSubPresets[index].mValues, sizeof(gSubPresets[index].mValues));
}